
<template>
    <stack-modal
        :show="is_show"
        @close_donation_modal="closeModal"
        :saveButton="{
            visible: false,
        }"
        :cancelButton="{
            visible: true,
            title: 'Close',
            btnClass: { 'btn btn-secondary': true },
        }"
    >
        <template #header>
            <h5 class="modal-title">
                <i class="fas fa-heart text-danger mr-2"></i>
                Support Our Work
            </h5>
        </template>
        
        <div class="donation-modal-content">
            <p class="text-muted mb-4">
                Your donation helps us continue creating quality content. 
                Choose an amount to donate:
            </p>
            
            <!-- Predefined Amount Buttons -->
            <div class="donation-amounts mb-4">
                <button 
                    v-for="amount in predefinedAmounts" 
                    :key="amount"
                    @click="selectAmount(amount)"
                    :class="['btn donation-btn', selectedAmount === amount ? 'btn-primary' : 'btn-outline-primary']"
                >
                    {{ amount }} GEL
                </button>
            </div>
            
            <!-- Custom Amount Input -->
            <div class="custom-amount-section mb-4">
                <label for="customAmount" class="form-label">Or enter custom amount:</label>
                <div class="input-group">
                    <input 
                        type="number" 
                        id="customAmount"
                        v-model="customAmount"
                        @input="onCustomAmountInput"
                        class="form-control" 
                        placeholder="Enter amount"
                        min="1"
                    >
                    <span class="input-group-text">GEL</span>
                </div>
            </div>
            
            <!-- Selected Amount Display -->
            <div class="selected-amount-display mb-4">
                <strong>Selected: {{ displayAmount }} GEL</strong>
            </div>
            
            <!-- Donate Button -->
            <button 
                @click="processDonation"
                :disabled="!isValidAmount || loading"
                class="btn btn-success btn-lg w-100 donate-submit-btn"
            >
                <span v-if="loading" class="spinner-border spinner-border-sm mr-2"></span>
                <i v-else class="fas fa-donate mr-2"></i>
                {{ loading ? 'Processing...' : 'Donate ' + displayAmount + ' GEL' }}
            </button>
            
            <!-- Success Message -->
            <div v-if="donationSuccess" class="alert alert-success mt-3">
                <i class="fas fa-check-circle mr-2"></i>
                Thank you for your donation!
            </div>
            
            <!-- Error Message -->
            <div v-if="errorMessage" class="alert alert-danger mt-3">
                <i class="fas fa-exclamation-circle mr-2"></i>
                {{ errorMessage }}
            </div>
        </div>
    </stack-modal>
</template>

<script>
import StackModal from '@innologica/vue-stackable-modal';

export default {
    name: 'DonationModalComponent',
    
    components: {
        StackModal,
    },
    
    props: {
        show: {
            type: Boolean,
            default: false,
        },
        articleId: {
            type: [Number, String],
            default: null,
        },
        articleTitle: {
            type: String,
            default: '',
        },
    },
    
    data: function() {
        return {
            is_show_donation_modal: false,
            predefinedAmounts: [5, 10, 20, 50, 100, 200],
            selectedAmount: null,
            customAmount: null,
            loading: false,
            donationSuccess: false,
            errorMessage: '',
        };
    },
    
    computed: {
        displayAmount() {
            if (this.selectedAmount !== null) {
                return this.selectedAmount;
            }
            if (this.customAmount !== null && this.customAmount !== '') {
                return parseFloat(this.customAmount).toFixed(2);
            }
            return '0';
        },
        
        isValidAmount() {
            const amount = parseFloat(this.displayAmount);
            return amount > 0;
        },
    },
    
    watch: {
        show(newVal) {
            this.is_show_donation_modal = newVal;
        },
    },
    
    mounted() {
        this.is_show_donation_modal = this.show;
    },
    
    methods: {
        selectAmount(amount) {
            this.selectedAmount = amount;
            this.customAmount = null;
            this.errorMessage = '';
            this.donationSuccess = false;
        },
        
        onCustomAmountInput() {
            this.selectedAmount = null;
            this.errorMessage = '';
            this.donationSuccess = false;
        },
        
        closeModal() {
            this.is_show_donation_modal = false;
            this.$emit('close');
            this.resetForm();
        },
        
        resetForm() {
            this.selectedAmount = null;
            this.customAmount = null;
            this.loading = false;
            this.donationSuccess = false;
            this.errorMessage = '';
        },
        
        async processDonation() {
            if (!this.isValidAmount) {
                this.errorMessage = 'Please select or enter a valid donation amount';
                return;
            }
            
            this.loading = true;
            this.errorMessage = '';
            this.donationSuccess = false;
            
            const amount = parseFloat(this.displayAmount);
            
            try {
                const response = await axios.post('/set_donation/process', {
                    amount: amount,
                    article_id: this.articleId,
                    article_title: this.articleTitle,
                });
                
                console.log('Donation processed:', response.data);
                this.donationSuccess = true;
                
                // Close modal after short delay
                setTimeout(() => {
                    this.closeModal();
                }, 2000);
                
            } catch (error) {
                console.error('Donation error:', error);
                this.errorMessage = error.response?.data?.message || 'An error occurred. Please try again.';
            } finally {
                this.loading = false;
            }
        },
    },
};
</script>

